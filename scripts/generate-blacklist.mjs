// Generate blacklist: real leaks + exact duplicates + known-dumb patterns.
// Writes to src/qualityBlacklist.js. Image-only categories (logos, who_*) are excluded
// since their "q" field is a prompt/domain, not what users read.

import fs from "node:fs";

const report = JSON.parse(fs.readFileSync("audit-report.json", "utf8"));

const IMAGE_CATS = new Set([
  "logos",
  "who_footballer","who_tv_character","who_anime_character","who_movie_character",
  "who_country_landmark","who_dinosaur","who_influencer","who_historic_figure",
  "who_animal","who_movie","who_tv_show",
  "flags","country_map","movie_scenes","songs","movie_show_emoji","country_emoji","general_emoji",
  "charades_general","charades_movies","charades_scenarios",
]);

const entries = [];
const seen = new Set();

function addEntry(cat, q, a, reason) {
  const key = `${cat}|${(q||"").toLowerCase().replace(/\s+/g," ").trim()}|${(a||"").toLowerCase().replace(/\s+/g," ").trim()}`;
  if (seen.has(key)) return;
  seen.add(key);
  entries.push({ cat, q, a, reason });
}

// 1. Real leaks (exclude image cats where q is a prompt like "Guess the X from the image")
for (const l of report.issues.leaks) {
  if (IMAGE_CATS.has(l.cat)) continue;
  // Skip false positives where answer word is a generic article or number
  const a = (l.a || "").toLowerCase();
  if (/^\d+$/.test(a) || ["the","a","an","of","and"].includes(a)) continue;
  addEntry(l.cat, l.q, l.a, "leak");
}

// 2. True internal duplicates — same question stem, same answer, same tier
for (const d of report.issues.internalDupes) {
  // Skip image cats — same q+a with different wiki is not a dup
  if (IMAGE_CATS.has(d.cat)) continue;
  // Only mark as dupe when BOTH question stems are the same (not just "similar" heuristic)
  const qA = (d.q || "").toLowerCase().replace(/[^a-z0-9 ]/g,"").replace(/\s+/g," ").trim();
  const qB = (d.prevQ || "").toLowerCase().replace(/[^a-z0-9 ]/g,"").replace(/\s+/g," ").trim();
  // Split into words, compare sets
  const aWords = new Set(qA.split(/\s+/).filter(w=>w.length>2));
  const bWords = new Set(qB.split(/\s+/).filter(w=>w.length>2));
  if (!aWords.size || !bWords.size) continue;
  const overlap = [...aWords].filter((w) => bWords.has(w)).length;
  const minLen = Math.min(aWords.size, bWords.size);
  // Dupe if 70%+ of words overlap AND same tier
  if (d.tier === d.prevTier && overlap / minLen >= 0.7) {
    addEntry(d.cat, d.q, d.a, "dupe");
  }
}

// 3. Known-dumb pattern matches
for (const x of report.issues.dumbPatterns) {
  addEntry(x.cat, x.q, x.a, "dumb pattern");
}

// Output
const source = `// Auto-generated by scripts/generate-blacklist.mjs
// Blacklist of (cat|q|a) keys. Entries matching these are filtered from BANK.
// Image-only categories excluded (logos, who_*, flags, charades) — their "q" is a
// prompt, not player-visible text.

const entries = ${JSON.stringify(entries, null, 2)};

function normalizeKey(cat, q, a) {
  return [cat, (q || "").toLowerCase().replace(/\\s+/g, " ").trim(), (a || "").toLowerCase().replace(/\\s+/g, " ").trim()].join("|");
}

export const QUESTION_BLACKLIST = new Set(entries.map((e) => normalizeKey(e.cat, e.q, e.a)));
export function isBlacklisted(catId, q, a) {
  return QUESTION_BLACKLIST.has(normalizeKey(catId, q, a));
}

export default QUESTION_BLACKLIST;
`;

fs.writeFileSync("src/qualityBlacklist.js", source);

const byReason = {};
for (const e of entries) byReason[e.reason] = (byReason[e.reason] || 0) + 1;
console.log(`Blacklisted ${entries.length} entries:`);
for (const [r, c] of Object.entries(byReason)) console.log(`  ${r}: ${c}`);
